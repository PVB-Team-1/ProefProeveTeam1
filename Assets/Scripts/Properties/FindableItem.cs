using UnityEngine;

/// <summary>
/// This class represents an item that can be found.
/// </summary>
[System.Serializable]
public class FindableItem
{
	/// <summary>
	/// The model of this item as type GameObject.
	/// </summary>
	public GameObject model;

	/// <summary>
	/// The icon of this item as type Sprite.
	/// </summary>
	public Sprite icon;
}